# Маленький процент пользователей готовых покупать расширенные функции. 
# Не очень выгодный формат, потому что чаще всего расширенные функции работают за единоразовый платеж. 
# Подписка 
# Работает по тому же принципу, что и фриум, но с акцентом на то, что обновлять подписку надо регулярно. 

# Среди всех способов монетизации приложений этот метод самый популярный в Play Market и App Store. 
# Какая-то часть контента или функций доступны бесплатно, за дополнительные возможности регулярно 
# списывается оплата.  

# К примеру, приложение для тренировки дыхания Prana Breath предлагает пользователям бесплатную 
# версию без рекламы и подписку Guru. За плату доступна расширенная база методов дыхания, 
# бесконечные тренировки, детальный график. В нем несколько вариантов подписки: 3 месяца, год, навсегда. 
# Оплаты создана так, чтобы тариф за год был наиболее выгодным. 

# Плюсы:

# Лояльность пользователей, повышение LTV. 
# Регулярные платежи. 
# Лояльность от Play Market и App Store. Снижается процент, который платформа забирает себе. 
# Минусы:

# Технические сложности с реализацией и отслеживанием подписки.
