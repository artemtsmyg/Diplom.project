# Не всегда получается создать контент за который пользователь готов регулярно платить.  
# Пробный период 
# Необходимо ввести код активации или использовать пробный период от 3-х до 30 дней в зависимости 
# от приложения. И на это время все, включая премиум функции, открыто. Для использования обязательно 
# требуется регистрация. 

# Особенность такого способа в том, что после использования пробного периода, приложение больше 
# недоступно. 

# Пример, «Meditopia:Сон, Медитация, музыка для сна» предлагает пробный премиум на 3 дня. 
# В него входит доступ ко всем материалам, более 500 вариантов медитаций, отслеживание прогресса, 
# офлайн режим для занятий, регулярные обновления. Как только завершается пробный период, доступны 
# только базовые медитации.  

