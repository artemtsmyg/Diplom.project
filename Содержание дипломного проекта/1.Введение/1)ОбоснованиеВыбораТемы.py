                # 1.Актуальность и повседневное применение: 

# Сегодня сложно переоценить роль мобильного телефона в жизни человека. С помощью этого компактного 
# устройства мы в любой момент можем быстро связаться со своими близкими и коллегами по работе, 
# чтобы узнать интересующую нас информацию. В списке контактов могут одновременно храниться тысячи 
# номеров и человек с лёгкостью может быстрой найти нужный в несколько нажатий. 
# Также на мобильных устройствах, помимо контактов может храниться и другая информация, различные файлы, 
# данные о кредитных картах и мультимедиа. [2]
# Люди очень много времени проводят, именно за своим смартфоном, причём даже когда он не используется 
# продолжается его работа. Телефон важен в повседневной жизни. Самыми распространенными платформами 
# являются:
# Android – бюджетная и самая распространённая сейчас.
# IOS – очень проста в использовании, не из дешёвых.
# Windows Mobile – телефоны на основе Windows, многосторонняя, достаточно много функций.
# Мобильные устройство — это любое небольшое устройство, которое обычно содержит дисплей и миниатюрную 
# клавиатуру (позже был изобретён сенсорный экран с виртуальной клавиатурой). 
# Первоначально это были в основном карманные устройства, но разнообразие таких устройств постоянно 
# увеличивается. Устройства становятся более гибкими и могут выполнять различные функции, 
# такие как запись и воспроизведение мультимедиа, подключение к видеочатам, подключение к Интернету, 
# функции оплаты.
# Мобильное приложение – программное обеспечение, предназначенное для работы на смартфонах, планшетах 
# и других мобильных устройствах, разработанное для конкретной платформы. [4]
# Первоначально мобильные приложения использовались для быстрой проверки электронной почты, но их 
# высокий спрос привел к расширению их назначений и в других областях, таких как игры для мобильных 
# телефонов и GPS, общение, просмотр видео и пользование интернетом.
# На сегодняшний день мобильные приложения выполняют массу задач в различных сферах нашей жизни. 
# С их помощью, человек обретает гораздо больше возможностей, например, в области бизнеса. 
# Сложно представить хотя бы немного крупную компанию, не имеющую своего собственного мобильного 
# приложения. Причём данные приложения могут выполнять сразу несколько профессиональных функций.
# Существует, также, множество различных приложений, которые требует интернет – соединение, 
# так как популярность мобильного интернета растёт стремительными темпами. 
# В 2012 году пользователь в среднем проводил в мобильном интернете 74 минуты в день, но на 
# сегодняшний день это время может превышать 5 часов. [1]
# Большинство обладателей современных смартфонов имеют целый список установленных на их устройстве 
# приложений. В основном их используют как элемент досуга, например, для общения или получения новой 
# актуальной информации. Но существуют и деловые приложения, к примеру приложения для финансовых 
# учреждений и банков с их помощью можно обменивать валюту, торговать недвижимостью и 
# заниматься онлайн – продажами.
# Для того, чтобы разделять приложения по их техническим характеристикам существуют выделенные категории. 
# Разделение происходит, исходя из того, для какой целевой аудитории разрабатывалось мобильное приложения.
# Наиболее востребованные категории можно разделить по исполняемым функциям:
# развлечения (игровые приложения, мультимедиа, музыка, заказ билетов онлайн);
# путешествия (заказ отеля, аренда авто, услуги гида, сервис онлайн – переводчика;
# бизнес (финансовые приложения, планирование, торговля, приложения для города, поиск работы);
# социальные приложения (социальные сети, глобальные брендированные сети, специализированные сети);
# еда (заказ и доставка еды, геолокация заведений питания);
# спорт (спортивные новости, покупка билетов на спортивные мероприятия, игровые симуляторы);
# образование (обучающие программы, интерактивные курсы);
# новости (дайджесты, рейтинги);
# стиль жизни (кулинарные приложения, планирование отпуска, рукоделие). 

#              2.Рост рынка мобильных приложений: 

# Рынок мобильных приложений постоянно расширяется, 

# предоставляя новые возможности для инноваций и предпринимательства. 

# Понимание процесса разработки и успешного внедрения мобильных приложений 

