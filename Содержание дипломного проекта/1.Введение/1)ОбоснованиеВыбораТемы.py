#                           Общие показатели:

# Сегодня использование приложений находится на рекордно высоком уровне. В 2022 году пользователи 
# скачали на 11% больше приложений, чем в 2021, а среднее время, проведенное в мобильных приложениях, 
# увеличилось на 3% и составило 5 часов в день. В то же время, пользователи все неохотнее тратят 
# деньги в приложениях (-2% по сравнению с 2021 годом). В основном это коснулось игр и почти не 
# затронуло приложения, которые предоставляют более необходимые функции и услуги.
# Зато широкая аудитория приложений, качественный таргетинг и другие преимущества in-app рекламы 
# привлекают все больше рекламодатели, что показывает рост расходов на рекламу в приложениях 
# в 2022 году на14%. По всем прогнозам, этот показатель продолжит расти и в этом году. 
# Это говорит о том, что при составлении стратегии монетизации своего приложения в первую очередь 
# стоит рассчитывать на доход от размещения рекламы.
# Gaming
# Спрос на игры находится на рекордно высоком уровне.
# По сравнению с 2021 годом в 2022 было скачано на 6,5 миллиардов больше игр, таким образом, 
# количество скачиваний составило почти 90 миллиардов.
# Однако расходы на внутриигровые покупки снизились на довольно существенные 5%.
# Лидером по пользовательским расходам по прежнему является категория RPG игр.
# На эту категорию приходится 31% от всех внутриигровых расходов. Но по количеству скачиваний эта 
# категория находится в конце списка, составляя только 3% от общего количества загрузок.
# На втором и третьем месте по пользовательским расходам расположились категории Strategy и Match, 
# на которые приходятся 15% и 11% внутриигровых расходов соответственно.
# Стоит заметить, что эти три категории, хоть и занимают лидирующие позиции, но в 2022 показали 
# снижение внутриигровых расходов: RPG на 8,3%, Strategy на 6,6%, Match на 5,4%.
# Единственная категория игр в которой потребительские расходы значительно выросли – Action игры +20,6%, 
# а лучшей игрой в этой категории является Honor of Kings – именно она принесла больше всего доходов 
# от игроков.
# По количеству загрузок лидирую категории Hypercasual – 29%, Simulation – 17% и Action – 11%.
# По количеству игровых часов лидирует категория Simulation, а точнее ее подкатегория 
# Creative Sandbox games с такими играми как Roblox и Minecraft. Время, проведенное в них, 
# выросло на 25% относительно 2021 года.
# Худшими по этому показателю оказались Shooting (Battle Royale) игры – количество затраченного 
# времени сократилось примерно на 20% по всему миру. Хотя в некоторых регионах время проведенное 
# в играх данной категории показало значительный рост в 2022 году: в Китае на 15%, а в Саудовской 
# Аравии на 45%.
# Finance
# Начавшееся в 2020 году активное продвижение финансовых приложений продолжило свой стремительный 
# рост в 2022. Мобильные банки, а также электронные кошельки и платежи набирают 
# популярность во всем мире. Так, из Топ-10 финтех приложений по количеству скачиваний в мире, 
# 6 приложений относятся к категории Digital Wallets & Payment.
# Экономические проблемы, такие как высокая инфляция, обуславливают процветание приложений 
# персонального кредитования в Северной Америке, Азии и Африке.
# После потрясений на криптовалютном рынке в 2022 можно было наблюдать заметный спад количества 
# скачиваний приложений для торговли криптовалютой.
# В отличие от других экономических приложений, финтех остается сильно локализованным, ведь здесь 
# особенно важно знание местного рынка, финансовых правил и норм. Особенно это заметно в 
# странах Азиатско-Тихоокеанского региона (APAC). Так, например, в Китае более 99% загрузок 
# финансовых ресурсов приходится на долю местных издателей.
# Продолжают свое активное развитие необанки. Хотя количество пользователей традиционных банков 
# все еще превышает количество пользователей необанков, в некоторых регионах, например в Великобритании, 
# необанкам удалось существенно сократить этот разрыв. В США компании предоставляющие банковские 
# услуги только в цифровом формате пока не сумели повторить успех Великобритании, но, в любом случае, 
# конкуренция со стороны необанков стимулировала традиционные банки на улучшение своих сайтов и 
# мобильных приложений. Это вызвало рост активных пользователей и увеличение проведенного времени в 
# большинства мобильных банковских приложениях.
# Еще одна значимая тенденция финтех рынка США 2022 года – увеличение количества загрузок Cash Advance 
# приложений. В последние годы такой тип приложений стал обычным явлением для США. 
# Это связывают с высоким уровнем инфляции. Но в 2022 году количество скачиваний резко возросло, 
# увеличившись на 44% по сравнению с 2021 годом.
# Retail
# Начавшийся во время пандемии стремительный рост покупок через мобильные приложения продолжается, 
# но его темпы замедлились.
# Время проведенное в приложениях для шопинга в 2022 году увеличилось на 9%, в то время как 
# в 2021 году этот показатель составил 19%.
# Сегодня наиболее растущими рынками мобильных приложений для покупок являются развивающиеся 
# рынки Азиатско-Тихоокеанского региона – Индия и Индонезия, а также страны Латинской Америки, 
# включая Бразилию, Мексику и Аргентину.
# Изменились мотивы покупателей. Во время пандемии покупатели совершали покупки в мобильных приложениях, 
# чтобы избежать личного присутствия и контакта. Сейчас очные покупки вернулись, но из-за высокой 
# инфляции покупатели идут в мобильные приложения в надежде сэкономить. Об этом также свидетельствует 
# увеличение в 2022 году на 27% количества скачиваний приложений для купонов и вознаграждений.
# Video Streaming
# Эксклюзивный спортивный контент служит важнейшим этапом для привлечения новых пользователей. 
# Добавление освещения крупных спортивных событий хотя и дорогостоящий, но высокоэффективный способ 
# привлечения новых пользователей потоковых видеосервисов.
# Неспортивный контент, который вызвал наибольший всплеск скачиваний приложений – "Эйфория" (HBO Max), 
# "Хэллоуин заканчивается” (Peacock TV) и "Дом дракона" (HBO Max).
# TikTok стал вторым не игровым приложением, в котором потребительские расходы превысили $6 миллиардов 
# долларов за все время, и значительно превзошел успех других развлекательных приложений.

#                         3.Образовательные цели: 

# Исследование процесса разработки мобильных приложений 
# предоставляет уникальную возможность углубленного изучения 
# современных технологий программирования, дизайна и тестирования, 
# что важно для моего профессионального и личностного развития.
# Современные тенденции, связанные с информатизацией и цифровизацией социальной и профессиональной 
# сфер нашли отражение в образовательной плоскости и направляют педагогическую науку на глубокое 
# осмысление воздействий на познавательные, визуальные, деятельностные и иные сферы личности обучаемого. 
# Использование мобильных контентов в социальной сфере привело педагогическому анализу возможностей 
# их применения в образовательной практике, в том числе, в учреждениях высшего образования с двух 
# направлений – как мобильного обучения, как вида обучения, так, и как использования мобильных 
# приложений в качестве не только средств получения информации, но и средств обучения и контроля.

# В современной педагогической науке педагогический анализ мобильного образования нашёл отражение в 
# трудах ряда ученых: с точки зрения перспектив применения в системе дистанционного обучения [1], 
# с точки зрения SMS-тестирования [8], как реализация принципа мобильного обучения или обучения в 
# любом удобном месте и в любое удобное время [6], как фактор профессиональной мобильности [10] и др.

# Рядом исследователей показана реализация дидактических функций посредством применения в 
# образовательном процессе мобильных приложений:

# познавательная, направленная на удовлетворение интеллектуальных, профессиональных, информационных и 
# иных потребностей;
# диагностическая, направленная на выявление способностей обучаемых;
# адаптационная, предполагающая проектирование собственной траектории обучения;
# ориентационная, направленная на практическую подготовку к профессиональной деятельности;
# контролирующая, выявляющая образовательные дефициты у обучающегося [3].
# В общем понимании мобильные приложения представляют собой специально разработанные программы, с 
# возможностью установки на той или иной платформе, обладающие определенным функционалом, 
# позволяющим выполнять различные действия и позволяющие трансформировать образовательный процесс, 
# не ограничивая традиционными инструментами, предоставляя неограниченные возможности [9]. 
# Причем, эти возможности направлены на создание наиболее комфортных условий реализации образовательного 
# процесса вне учебного заведения посредством обеспечения доступа к информационным ресурсам [2].

# С помощью таких мобильных приложений обучающиеся имеют возможность решения ряда образовательных 
# задач [4]:

# организация персональной образовательной среды в контексте реализации личностно-ориентированного 
# подхода с учетом индивидуальных образовательных возможностей обучаемого;
# получение доступа к источникам учебной, справочной и иной информации;
# анализ информации, её структурирование, обобщение, вычленение главного;
# организация и реализация проектной работы и проектной деятельности;
# проведение тестирований, опросов и иных различных типов аттестаций;
# подстройка под образовательные потребности и возможности обучаемого;
# возможность отслеживания образовательной динамики;
# персонализация образовательных контентов и иные.

