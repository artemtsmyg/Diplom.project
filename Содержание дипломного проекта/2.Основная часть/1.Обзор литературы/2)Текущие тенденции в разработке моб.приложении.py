#         2.Увеличенная реальность (AR) и виртуальная реальность (VR): 

# AR и VR становятся все более популярными в мобильных приложениях, 

# используя их для игр, образования, обучения и симуляции.

# С помощью искусственного интеллекта создаются интерактивные миры, в которых с различной степенью 
# сходства имитируется человеческая реальность.

# Ощущения передаются пользователю через его органы чувств, прежде всего зрение, слух, осязание. 
# Для полноты картины могут подключаться даже запахи.

# Поискать в своём городе покемона, примерить платье онлайн, поучаствовать в битве фантазийных 
# персонажей, стать пилотом самолёта — всё это возможности, которые открывает виртуальная реальность. 
# И разработчики создают всё новые способы применения VR и AR не только в качестве развлечения, 
# но и в других сферах жизни.

# Принцип работы технологии виртуальной реальности
# Параллельно развиваются три типа искусственной реальности:

# виртуальная;
# дополненная;
# смешанная.
# У каждого из них — свои особенности. Смешанная, или Mixed Reality (MR), — вариант гибридного 
# применения обеих технологий.

# Virtual Reality:
# Виртуальная реальность полностью создаётся компьютером. Самый распространённый способ попасть 
# в VR — специальные очки или шлемы. Это гарнитура, которая проводным или беспроводным способом 
# подключается к компьютеру, игровой консоли или смартфону.

# Внутри расположены один или два ЖК-дисплея, часто применяют Super-fast LCD и OLED. 
# Масштаб картинки и настройка стереоскопического изображения регулируется встроенными линзами. 
# Традиционно угол обзора составляет около 100 градусов — этого достаточно, чтобы устранить 
# эффект экрана и полностью погрузить в искусственный мир.

# На дисплее видео отображается с частотой кадров от 60 до 120 в секунду. Минимальное время задержки, 
# как правило, не более 50 мс.

# Фокусировка взгляда, размытие переднего или заднего плана, движение картинки вслед за глазами 
# усиливают эффект присутствия. Это возможно благодаря тому, что ИК-датчики внутри гарнитуры отслеживают взгляд. Такая функция есть не во всех представленных на рынке шлемах.

# Ещё одна составляющая технологии — трекинг. Это отслеживание движения головы и тела пользователя. 
# Есть два способа трекинга:

# Внутренний. Датчики и широкоугольные камеры с подсветкой располагаются непосредственно на шлеме и 
# фиксируют положение тела и окружающих предметов. Основные считывающие движения устройства — гироскоп 
# и акселерометр. Метод может быть неточным: система не всегда распознаёт руки за спиной или 
# скрещённые ноги. Проблему решают контроллеры, которые пользователь держит в руках. 
# Они не только фиксируют движения, но и позволяют управлять инвентарём.
# Внешний. В помещении напротив друг друга на штативах или других возвышенностях располагаются 
# датчики-маяки, которые замыкают игровое пространство. С помощью высокочастотных миганий 
# маяки постоянно контролируют малейшие изменения положения тела игрока и передают данные 
# компьютеру для корректировки картинки на дисплее. Способ максимально точный и эффективный.
# Некоторые производители предлагают дополнительные устройства для комфортного нахождения в 
# вымышленной среде — «умные» перчатки, беговые дорожки для имитации ходьбы и даже оборудование 
# для управления средой голосом.

# Технологии прогнозируют стремительный рост. Уже скоро в свободном доступе могут появиться VR костюмы, 
# в разы увеличивающие степень погружения и остроту ощущений.

# Чаты, социальные сети, ивенты в других измерениях станут доступнее. 
# Любой пользователь сможет присутствовать на них в виде аватара.

# Augmented Reality:
# В дополненной реальности графическая картинка накладывается на реальную и создаётся единая плоскость, 
# которую видит пользователь на экране своего устройства.

# С технологией знаком любой пользователь социальных сетей — различные фильтры и маски на видео 
# позволяют создавать интересный запоминающийся контент. Прицелы на военной технике, 
# выведенные на экран бортовым компьютером, — это тоже дополненная реальность.

# Принцип работы основан на обучении нейросети. Она учится распознавать объекты и маркеры реальности, 
# а также фиксировать местоположение.

# По загруженной библиотеке образов с описанием их общих черт сеть распознаёт, например, 
# глаза человека или автомобиль на улице. Если нужно расположить графику в конкретном месте, 
# на него фиксируется маркер — цифровой код. При наведении камеры на эту точку в кадре появится 
# нужное изображение.

# Всё окружающее пространство искусственный интеллект разделяет на карту точек и плоскостей, 
# по которым может определить местоположение пользователя.

# С помощью AR-технологии любое мобильное приложение можно дополнить полезными опциями, например, 
# возможностью измерять размеры и расстояния без реальной рулетки или видеть карту звёзд прямо 
# на небе в режиме реального времени. И создать это не так сложно, как кажется.

# В открытом доступе есть различные сервисы, которые способны точно определять расположение 
# камеры и размещать в выделенной точке графические 3D-объекты.

# Например, инструмент Naviar SDK от команды разработчиков SberDevices позволяет интегрировать 
# дополненную реальность в любой проект.

# Сходство между виртуальной и дополненной реальностью
# Сходство VR и AR выражено в обобщённых чертах:

# Созданы с использованием схожих технологий. Прежде всего это трекинг, который позволяет 
# определить положение пользователя в пространстве и направление фокуса его объектива.
# Служат для одних и тех целей — развлекательных, познавательных, исследовательских, обучающих — и 
# в целом улучшают качество жизни людей.
# Стали результатом развития IT-технологий и совершенствуются параллельно.
# Несут в себе большой потенциал для новых прорывов в науке, медицине, промышленности и других сферах.
# В остальном это два разных направления IT-индустрии с очевидным разграничением сфер применения 
# и с различной доступностью для пользователей.

# Различия между виртуальной и дополненной реальностью
# Виртуальная реальность создаёт мир без границ. Экран, расположенный перед глазами, 
# ведёт пользователя по выдуманному миру, полностью блокируя настоящий. В AR графика 
