#            3.Интернет вещей (IoT): 

# Начнем с того, что определим, что такое IoT (интернет вещей)
# Интернет вещей (IoT) — это об огромном количестве «вещей», которые подключены к Интернету 
# для обмена данными с другими вещами — приложениями IoT, подключенными устройствами, 
# промышленными машинами и многим другим. Устройства, подключенные к Интернету, используют 
# встроенные датчики для сбора данных и, в некоторых случаях, воздействуют на них. 
# Подключенные к IoT устройства и машины могут улучшить нашу работу и жизнь. 
# Примеры применения Интернет вещей (IoT): от умного дома, который автоматически 
# регулирует отопление и освещение, до умной фабрики, которая контролирует промышленные 
# машины для поиска проблем, а затем автоматически настраивается, чтобы избежать сбоев.

# Насколько мобильные приложения с улучшенными функциями Интернет вещей (IoT) влияют на бизнес?
# Приложения с поддержкой Интернет вещей (IoT) вошли почти во все вертикали и кардинально 
# изменили способ функционирования бизнесов. Конечно, мобильное приложение — это только часть 
# инфраструктуры Интернет вещей (IoT).

# Однако в сочетании с новыми техническими достижениями и технологиями на данный момент это может 
# повысить ваши продажи и уровень производительности, а также полностью изменить 
# способ ведения бизнеса.

# Доступность: мобильность мобильного телефона гарантирует, что его пользователь имеет 
# круглосуточный доступ к управлению системой IoT.
# Больше данных для анализа: системы Интернет вещей (IoT) собирают и обрабатывают данные с датчиков, 
# подключенных устройств и мобильных телефонов. Когда смартфон становится еще одним элементом 
# подключенной инфраструктуры, он предоставляет массу данных о привычках, предпочтениях и 
# образе жизни своих пользователей, что открывает двери для более персонализированных рекламных акций, 
# домашних настроек и других вариантов использования. Эти элементы в дальнейшем используются службами 
# разработки мобильных приложений для персонализации приложений.
# Расширенный пользовательский опыт (UX)
# Однако, стремясь сделать мобильные приложения удобными для конечных пользователей, 
# компании-разработчики приложений Интернет вещей (IoT) больше не сосредотачиваются исключительно 
# на предоставлении удобных интерфейсов. Сегодня мобильные приложения, входящие в сеть IoT, 
# создаются с учетом лучшей интеграции с другими подключенными устройствами. Они должны, 
# с одной стороны, взаимодействовать с другими частями системы; с другой стороны, ожидается, 
# что они осознают преимущества приложений, а именно портативность, интуитивно понятное
# использование и бесшовную интеграцию в повседневную рутинную деятельность.

# Ориентированные на потребителя приложения в промышленном IoT
# В далеком 2017 году Sundar Krish, генеральный директор Waygum.i, заявлил что:

# «Обычно считается, что промышленный IoT ускоряется за счет того, что несколько технологий и 
# продуктов становятся лучше и дешевле — в сенсорах, периферийных вычислениях, облаке, 
# сетевых подключениях и пространствах big data [...] Однако одна мега-тенденция, 
# которая на самом деле способствует гораздо больше для Industrial IoT, 
# хотя и недооценивается, — это мобильные приложения».

# Причина понятна: сегодня почти у каждого человека, включая промышленных рабочих, есть смартфон, 
# обеспечивающий круглосуточный доступ к подключенной инфраструктуре. Этот взрывной рост 
# пользовательской базы можно использовать по-разному.

# Удаленное наблюдение
# С помощью мобильного телефона любой подключенный продукт может быть доступен из любого места 
# в любое время.

# «Де-факто случай использования любой промышленной системы IoT — это мониторинг», — заявлял тот 
# же Сундар Криш (Sundar Krish).

# Де-факто (лат. de facto — «на деле», «фактически») — латинское выражение, означающее нечто 
# действительное, проверенное на опыте, но не закреплённое законом.

# Нефтегазовая промышленность, производственные предприятия, металлургические заводы — любая отрасль 
# может улучшить свою политику профилактического обслуживания, установив эффективное корпоративное 
# решение IoT и предоставив своим сотрудникам приложение для мониторинга в реальном времени поверх него. 
# Такие приложения представляют собой информационную панель, позволяющую пользователям просматривать 
# данные о производительности оборудования в виде диаграмм, диаграмм и графиков, а также получать 
# предупреждения, если что-то пойдет не так.

# Клиенты также могут воспользоваться функцией удаленного мониторинга. Mercedes-Benz, один из 
# пионеров использования интеллектуальных технологий и облака в автомобилестроении, 
# продемонстрировал

# как это можно сделать с помощью приложения Mercedes Me. Мобильное приложение предоставляет 
# пользователям удаленное управление автомобилем, а также подробную информацию для диагностики и 
# помощи в ремонте автомобиля.

# Мобильный рабочий процесс
# Автоматизация и оцифровка — вот два ключевых слова сегодняшней промышленной трансформации. 
# Интернет вещей предоставляет все необходимые инструменты повышения эффективности процессов 
# для этой трансформации, повышая производительность труда сотрудников.

# Уж простите, приведу пример из европейских компаний. А точнее из Ирландии. 
# Мы кстати уже длительное время работаем с западными компаниями, учимся у них опыта и 
# предоставляем мобильные приложения на их рынок.

# Пример: электростанции Уайтгейт компании Bord Gáis Energy в Ирландии. 
# Компания использует передовое решение управления производительностью активов (APM) для оцифровки 
# объекта. Благодаря платформе, электростанция имеет круглосуточный мониторинг с 
# помощью более 140 датчиков, расположенных по всему месту.

# Промышленные рабочие имеют сводное представление о производительности предприятия вместе с 
# предупреждениями о непредвиденных проблемах, чтобы предприятие могло предотвратить крайне 
# нежелательные и дорогостоящие простои. Вся эта информация отображается на экранах компьютеров 
# и мобильных телефонов.

# Продвижение периферийных вычислений
# Мобильные приложение может быть добавлено к массе датчиков, объединенных в экосистему IoT, 
# и может служить как модемом, так и периферийным устройством.

# Интеграция Интернет вещей (IoT) в финансовый сектор:

# В последнее время много говорится о слиянии IoT и Blockchain. В то время как для первого 
# необходим безопасный метод автоматизации процессов и обмена данными, последний может обеспечить 
# требуемый уровень безопасности для управления передачей данных между подключенными устройствами.

# Мобильные приложения на основе блокчейн имеют множество преимуществ для среды IoT. 
# Они децентрализованы (информация не хранится в одном объекте), неизменяемы 
# (без изменений записи в реестры могут производиться без согласия сети) и 
# неконтролируемы никакими властями. Эти характеристики позволяют гарантировать безопасность 
# по двум наиболее уязвимым темам — персональным данным и финансам.

# Мобильные приложения как часть инфраструктуры Интернет вещей (IoT) могут улучшить многие операции, 
# в том числе:

# Управление персональными данными в любом проекте IoT.
# Платежи и переводы в финансовых операциях.
# Подписание контрактов путем применения смарт-контрактов.
# Мониторинг здоровья в Smart Hospital и подключенных системах здравоохранения.
# Системы Интернет вещей (IoT), включающие транзакции, связанные с деньгами, 
# документооборотом или контрактами, выиграют только в том случае, если связанные с 
# ними мобильные приложения будут такими же безопасными и стабильными, как функционирование блокчейна.

# Успешный пример технологии блокчейн, интегрированной в IoT, а именно разработки 
# мобильных приложений — Chronicled. Приложение, поддерживаемое технологией блокчейн, 
# используется для безопасного отслеживания документов и изменений в них, таких как новые версии, 
# исправления и обновления статуса.

# Персонализированный пользовательский опыт в розничной торговле (e-commerce)
# Несмотря на то, что онлайн-покупки становятся все популярнее, обычные магазины остаются 
# привлекательными для клиентов. Они расширяют свое присутствие в Интернете и внедряют новые 
# стратегии продаж, в то время как интернет-магазины открывают физические магазины 
# (так называемая тенденция «кликов на кирпичи»).

# Приложения с поддержкой BLE, умные полки, сотрудники-роботы — это лишь некоторые из 
# элементов современных технологически продвинутых физических магазинов.

# Интернет вещей — это важный инструмент, влияющий на весь процесс совершения покупок. 
# Он открывает путь к новым типам данных из новых источников в соответствии с тенденцией 
# «Знай своего клиента» (KYC): счетчики трафика в магазинах, киоски, бирки инвентаря и мобильные 
# телефоны клиентов с установленными приложениями. Розничные торговцы научились использовать 
# мобильные приложения в своих подключенных системах.

# Навигация в магазине
# С помощью своих мобильных телефонов клиенты могут легко найти товары, которые они ищут. 
# Некоторые крупные розничные торговцы, такие как Lowe’s, уже внедрили эту технологию, 
# чтобы сделать покупки более удобными для своих клиентов. Мобильные приложения с поддержкой 
# Интернет вещей (IoT) ищут товары, добавляют их в список покупок и находят в магазине.

# Другие розничные торговцы делают еще один шаг вперед и предоставляют покупателям дополнительные услуги. 
# Например, мобильное приложение Sephora из простого помощника по навигации превратилось 
# в помощник в магазине. Когда покупатель входит в магазин, приложение предоставляет рекомендации 
# по продукту на основе профиля этого пользователя и истории покупок.

# Автоматизированная касса
# Киоски самообслуживания позволяют клиентам оплачивать свои покупки без вмешательства человека: 
# система настройки считывает теги на товарах и автоматически рассчитывает стоимость из 
# мобильного платежного приложения клиента.

# Такой подход экономит время покупателей, особенно в условиях нехватки времени, и экономит 
# деньги розничных продавцов, сокращая количество сотрудников и делая магазин более привлекательным. 
# Магазины Amazon Go были пионерами в этой технологической области; Теперь количество 
# розничных торговцев, которые запустили мобильные решения для автоматизированных систем кассы, 
# значительно увеличилось, и сегодня они включают таких гигантов розничной торговли, 
# как Walmart и Target. (Опять пример из запада)

# Персонализированные акции и скидки
# Мобильные приложения, взаимодействующие с датчиками в магазине и другими элементами системы IoT, 
# позволяют делать индивидуальные предложения по лучшим ценам. 
# Розничные продавцы могут отправлять скидки за лояльность, когда рядом обнаруживается определенный 
# покупатель, тем самым выстраивая с ними лояльные отношения.

#         4.Мобильная безопасность: 

# Развитие высоких технологий и тренд мобильности привели к тому, что современное мобильное устройство 
# зачастую используется в качестве мобильного офиса, центра развлечений и инструмента для потребления 
# интернет-контента. Сам аппарат многое может рассказать о своем владельце, 
# так как в его памяти хранятся: контакты коллег, друзей и близких с их персональными данными, 
# журнал звонков, переписка, параметры точек доступа Wi-Fi, которые расположены в пределах ареала 
