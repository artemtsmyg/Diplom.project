# Выбор пути мобильной разработки

# Приложения популярны не только среди современных пользователей интернета, но и достаточно 
# прибыльны для их владельцев. Если связать эти два фактора воедино, можно сделать вывод, 
# что практически любая стратегия развития бизнеса может включать создание приложения. 
# Дилемма, однако, заключается в выборе правильного пути разработки мобильных приложений.

# Одним из первых шагов на пути к цифровому успеху является решение о мобильной операционной 
# системе – это, кстати, было не так просто десять лет назад, когда Android, iOS, Microsoft, 
# RIM и Symbian были вполне жизнеспособными вариантами.

# Сегодня выбор гораздо проще, поскольку единственными крупными игроками остаются Android и iOS, 
# которые вместе составляют около 99% от общей доли рынка мобильных операционных систем.
# Согласно различным статистическим данным, Android выигрывает по количеству пользователей, 
# но нет недостатка и в сторонниках iOS, доля которого на рынке составляет 25,75%. 
# В то время как Google Play Store может похвастаться большим количеством приложений (2,5 млн), 
# Apple App Store содержит более 1.8 млн приложений. Одного этого факта достаточно, чтобы показать, 
# что ни одну из двух платформ не следует упускать из виду.

# image

# Поскольку выбор мобильной операционной системы является вопросом личных предпочтений пользователей, 
# а не вопросом производительности или доступности, будет целесообразно в конечном итоге создать 
# мобильное приложение как для Android, так и для iOS – и есть три способа сделать это.

# Отдельные нативные приложения для Android и iOS

# Нативное решение, как следует из названия, предполагает разработку приложения на родном 
# для данной платформы языке программирования: Java или Kotlin для Android, Objective-C или Swift 
# для iOS. Будучи глубоко ориентированной на операционную систему, разработка нативных приложений 
# имеет свои достоинства и недостатки. С одной стороны, нативное решение обеспечивает доступ ко 
# всем функциям данной ОС, позволяет неограниченно настраивать интерфейс и предотвращает любые 
# проблемы с производительностью. С другой стороны, если вы хотите охватить оба типа пользователей, 
# вам придется создать два отдельных приложения, которые требуют больше времени, денег и усилий.

# Прогрессивное веб-приложение

# Прогрессивное веб-приложение – технология в веб-разработке, которая добавляет сайтам возможности 
# приложений для мобильных устройств и трансформирует сайт в приложение. На выходе получаем гибрид 
# сайта и приложения для мобильных устройств. Однако, как любой другой вариант, прогрессивные 
# веб-приложения небезупречны, так как они потребляют больше энергии батареи и не могут получить 
# доступ ко всем функциям данного устройства, например, к календарю, камере, контактам и так далее. 
# Кроме этого, теряется возможность перекрестного входа в веб-приложение с помощью приложения Facebook, 
# Инстаграм, Вконтакте или т.д. Несмотря на то что веб-приложение не требует установки из 
# Google Play Store или Apple App Store, последние выполняют функцию крайне удобных 
# библиотек для пользователей.

# Одно кроссплатформенное приложение для двух систем

# Кроссплатформенность – это способность ПО (в нашем случае мобильных приложений) 
# работать на нескольких платформах.

# Кроссплатформенная мобильная разработка позволяет охватить две операционные системы, iOS и Android, 
# одним кодом. Она не предполагает написания кода на родном языке программирования, однако обеспечивает 
# почти нативный опыт благодаря интерфейсу визуализации с использованием собственных элементов 
# управления.

# На текущий момент многие компании используют кроссплатформенные решения, кто-то уже всерьез 
# подумывает перейти на них в ближайшем будущем. Это не только вендоры самих решений, как, например, 
# Facebook со своим React Native, на котором работают приложения Facebook и Instagram, но и другие 
# крупные игроки рынка, у которых имеются продукты, например, на Flutter – Alibaba, Philips Hue, 
# Hamilton, Tencent, Grab, Groupon и другие.

# Существует множество статей, где подробно анализируются все преимущества 
# кроссплатформенных приложений. Однако плюсы и минусы стоит рассматривать на платформе, 
# которая имеет все шансы стать в 2020 году самой популярной среди разработчиков – Flutter.

# Flutter

